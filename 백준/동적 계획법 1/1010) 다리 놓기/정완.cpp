/*

2 2     --> 2C2     = 2! / (2-2)! 2! = 2/2 = 1
1 5     --> 5C1     = 5! / (5-1)! 1! = 120 / 24 = 5;
13 29   --> 29C13   = 29! / (29-13)! 13! = 29! / 16! 13!

*/
#include "iostream"
#include "algorithm"

using namespace std;

int main() {
    int t;
   cin >> t;
	while (t--) {
		long long int ans = 1;
		int r = 1;
		int n, m;

		cin >> n >> m;

		int i = m;
		int j = n;

		while (j--)     // nCr = n! / (n-r)!r!
        {
			ans *= i--;
			ans /= r++;
		}

		cout << ans<<"\n";
	}
}